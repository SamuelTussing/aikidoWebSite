                    <h2>DEROULEMENT DES COURS</h2>
                    <p>Chaque cours d’aïkido commence par un moment de calme et de respect : le salut, ou rei, un rituel important pour marquer le début de la pratique et renforcer la concentration. Ensuite, les élèves participent à une série d’étirements et d’échauffements, essentiels pour préparer le corps et éviter les blessures.
                        Le cours se poursuit par la présentation d'une technique par l'enseignant. Celui-ci démontre chaque mouvement en expliquant ses principes et ses subtilités, afin que les élèves, quel que soit leur grade, puissent l’aborder avec compréhension. Les élèves travaillent ensuite en binôme pour mettre en pratique la technique, favorisant l’apprentissage par l’expérience et l’échange.
                        Il arrive également que le cours inclue une pratique d’armes, comme le bokken (sabre en bois) ou le jo (bâton), permettant d’explorer les bases de distance et de précision. Enfin, la séance se termine par des étirements pour relâcher les tensions et conclure dans la sérénité.</p>
