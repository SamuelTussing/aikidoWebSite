                    <h2>PROGRESSION POUR LES GRADES</h2>
                    <p>L’idée même de progression technique ou pédagogique est difficile à manier en Aïkido puisque toute la pratique n’est que répétitions et allers-retours incessants entre des “bases” et “variantes”.</p>
                    <br>
                    <p>Il faut bien pourtant aborder les différentes techniques et formes de travail dans un certains ordre, obéissant à une certaine logique. L’Association d’Aïkido de la Zorn se base sur la progression proposée par Paul Muller.</p>
                    <br>
                    <p>Les grades Kyu sont attribués, au sein du club, par le professeur, dans les premiers mois et années de pratique, avant l’obtention de la ceinture noire.</p>
                    <br>
                    <p>Les grades Dan sont délivrés, après plusieurs années de pratique, au cours d’examens organisés au plan régional, inter-régional ou national, en fonction des niveaux, devants des jurys inter-fédéraux.</p>
