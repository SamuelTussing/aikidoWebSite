                    <h2>SIGNIFICATION DE L'AIKIDO</h2>
                    <p>Aïkido peut donc se traduire par « la voie de la concordance des énergies ».</p>
                    <br>
                    <p>En effet, le terme « concordance » est plus près du sens japonais original de l’aiki comme étant une action de rencontre (explicité dans la composition du kanji) que le terme « harmonisation ». L’« harmonie » peut être le résultat souhaité de la pratique de l'aïkido, mais on ne fait pas d'aïkido sans faire concorder les énergies. Comme le fait remarquer Olivier Gaurin, l'aïkido, par la concordance (« mettre les cœurs ensemble »), amène à un résultat où il sera possible de communiquer avec l'« adversaire », chose impossible si on a dans l'idée de l'harmoniser (« amener à une entente, se mettre d'accord », ce qui peut être impossible) ou de le détruire. Un autre problème soulevé est qu'« harmonie » implique souvent une notion d'amitié ou de paix, ce qui est superflu (on ne peut pas être aimé par tout le monde, même si l'on aime soi-même tout le monde). Par exemple, les Japonais utilisent le mot wagō (和合) pour « harmonie », terme composé de « paix » et de « concorder » : en concordant vers la paix, on crée l'harmonie.</p>
                
